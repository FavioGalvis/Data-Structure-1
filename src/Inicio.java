    public void apilar_aleatorios(){
    int i, cant, u;
    Random rnd=new Random();
    cant=Integer.parseInt(JOptionPane.showInputDialog(null,"Ingrese cuantos numeros desea generar en la pila"));
    for(i=0;i<cant;i++){
        u=(int)(rnd.nextDouble()*10+1);
        pila.apilar(u);
    }
    pila.imp_pila();
    txtsalida1.setText(pila.salida);
    clean();
}

